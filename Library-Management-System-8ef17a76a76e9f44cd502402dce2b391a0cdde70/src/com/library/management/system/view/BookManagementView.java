    private void saveBook() {
        try {
            BookDto bookDto = new BookDto(txtBookID.getText(), txtTitle.getText(), txtAuthor.getText(),
                    txtCategoryID.getText(), Integer.parseInt(txtCopiesQoH.getText()));
            String resp = BookController.save(bookDto);
            JOptionPane.showMessageDialog(this, resp);
            clearForm();
            // loadTable();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error at save data");
            System.out.println(ex);
        }

    }

    private void clearForm() {
        txtBookID.setText("");
        txtTitle.setText("");
        txtAuthor.setText("");
        txtCategoryID.setText("");
        txtCopiesQoH.setText("");
    }

    private void updateBook() {
        try {
            BookDto UpdatedBookDto = new BookDto(txtBookID.getText(), txtTitle.getText(), txtAuthor.getText(),
                    txtCategoryID.getText(), Integer.parseInt(txtCopiesQoH.getText()));
            String resp = BookController.update(UpdatedBookDto);
            JOptionPane.showMessageDialog(this, resp);
            // loadTable();
            clearForm();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error at update Book");
            System.out.println(e);
        }
    }

    private void deleteBook() {
        try {
            String BookId = txtBookID.getText();
            String resp = BookController.delete(BookId);
            JOptionPane.showMessageDialog(this, resp);
            clearForm();
            // loadTable();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error at Delete Book");
            System.out.println(e);
        }
    }
